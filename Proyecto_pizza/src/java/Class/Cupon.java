package Class;

/**
 *
 * @author Mago
 */
public class Cupon {
    private int ID;
    private int estado;

    /**
     * @return the ID
     */
    public int getID() {
        return ID;
    }

    /**
     * @param ID the ID to set
     */
    public void setID(int ID) {
        this.ID = ID;
    }

    /**
     * @return the estado
     */
    public int getEstado() {
        return estado;
    }

    /**
     * @param estado the estado to set
     */
    public void setEstado(int estado) {
        this.estado = estado;
    }

    public Cupon(int ID, int estado) {
        this.ID = ID;
        this.estado = estado;
    }

    public Cupon() {
    }
    
    
    
}
